import turtle

meg_turtle = turtle.Turtle()

# Draw a square
# Function Definition

def draw_square():
    meg_turtle.forward(100)
    meg_turtle.right(90)

    meg_turtle.forward(100)
    meg_turtle.right(90)

    meg_turtle.forward(100)
    meg_turtle.right(90)

    meg_turtle.forward(100)
    meg_turtle.right(90)

    meg_turtle.forward(100)

draw_square()

